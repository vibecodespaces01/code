# Exemplo DE USO DE CLASSE EM PYTHON

# A primeira letra do nome da classe deve ser maiúscula por convenção
# Uma classe é um modelo para criar objetos
# Atributos são as características do objeto
# Métodos são as ações que o objeto pode realizar

class MinhaClasse:

    x = 5

mc1 = MinhaClasse()
print(mc1.x)

# Saída: 5
