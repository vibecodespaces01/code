# Crie uma função chamada saudacao que receba o nome de uma pessoa como parâmetro e exiba a mensagem: "Olá, [nome]! Seja bem-vindo(a)!"

# Função de saudação
def saudacao(nome):
    print(f"Olá, {nome}! Seja bem-vindo(a)!")

# Exemplo de uso
nome_usuario = input("Digite seu nome: ")
saudacao(nome_usuario)
